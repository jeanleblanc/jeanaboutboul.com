export function pickRandomScenario(previousId = null) {
  const pool =
    previousId && WEB_SCENARIOS.length > 1
      ? WEB_SCENARIOS.filter((scenario) => scenario.id !== previousId)
      : WEB_SCENARIOS;

  return pool[Math.floor(Math.random() * pool.length)];
}
